#Operator

#LOGICAL OPERATOR

#and
a = 5
print(a>3 and a<10)
# returns True because 5 is greater than 3 AND 5 is less than 10

#or
a=5
print(a>5 or a<10)
# returns True because one of the conditions are true

#not
x = 5
print(not(x > 3 and x < 10))
# returns False because not is used to reverse the result


